    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        try {
            $product = Product::create($request->all());
            $product->refresh();
            $message = ['message' => 'Product created!', 'product' => $product, 'getUrl' => route('product.show', $product->id), 'editUrl' => route('product.update', $product->id)];
        } catch (\Throwable $th) {
            $message = ['message' => 'Could not create the product!'];
        }

        return json_encode($message);
    }

    /**
     * Display the specified resource.
     *
     * @param  \App\Models\Product  $product
     * @return \Illuminate\Http\Response
     */
    public function show(Product $product)
    {
        $product = Product::find($product);

        return json_encode(['product' => $product]);
    }

    /**
     * Show the form for editing the specified resource.
     *
